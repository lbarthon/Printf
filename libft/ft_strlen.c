#include <string.h>

size_t	ft_strlen(const char *str)
{
	size_t i;

	i = 0;
	while (*str)
	{
		str += 4;
		i += 4;
	}
	while (*str == 0)
	{
		str--;
		i--;
	}
	return (i + 1);
}
