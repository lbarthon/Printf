#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i] && i < n)
		i++;
	return ((i != n) ? ((unsigned char)s1[i] - (unsigned char)s2[i]) :
			((unsigned char)s1[i - 1] - (unsigned char)s2[i - 1]));
}
